"""
AutoGen equivalent of examples/demo.ae
Pipeline: generate_users → filter_adults → summarize_report

This file exists for benchmarking purposes only.
It is NOT runnable without `pip install pyautogen`.

AutoGen is conversation-oriented. Implementing a deterministic, sequential
data pipeline requires explicit scaffolding that fights the framework's
design intent: each step must be serialized into a message, execution is
LLM-mediated by default, and there is no built-in data-flow or safety model.

Two implementation strategies are shown:

  A) AssistantAgent + code executor  — most idiomatic for AutoGen; each node's
     logic is generated by an LLM at runtime (non-deterministic, LLM tokens
     consumed per step).

  B) ConversableAgent with hard-coded replies — deterministic, but requires
     manually overriding AutoGen's message-passing with a function hook for
     every node. Loses all framework benefit.

The code below shows Strategy A (the honest use case) with inline notes on
the costs Strategy B would require.
"""

import autogen
from autogen import AssistantAgent, UserProxyAgent, GroupChat, GroupChatManager

# ── LLM configuration ─────────────────────────────────────────────────────────
# AutoGen requires an LLM config for each agent that generates or routes output.
# Tokens are consumed on EVERY step by the LLM inference call.

llm_config = {
    "model": "gpt-4o-mini",
    "temperature": 0,
    # NOTE: "temperature": 0 reduces but does NOT eliminate non-determinism.
    # Different model versions, API changes, or sampling edge-cases can vary output.
}

# ── Agents ─────────────────────────────────────────────────────────────────────
# Each agent is declared with a natural-language system prompt.
# Safety, output schema, and validation are expressed as prompt instructions —
# there is no structural enforcement.

orchestrator = UserProxyAgent(
    name="Orchestrator",
    human_input_mode="NEVER",
    max_consecutive_auto_reply=10,
    is_termination_msg=lambda m: "PIPELINE_COMPLETE" in (m.get("content") or ""),
    code_execution_config={"use_docker": False},
    # NOTE: code_execution_config enables arbitrary code execution.
    # There is no safety level gate — all code runs without capability checks.
)

data_agent = AssistantAgent(
    name="DataGenerator",
    system_message=(
        "You are a data generation agent. "
        "When asked to generate users, write and execute Python code that "
        "produces exactly this list: "
        '[{"id":1,"name":"Alice","age":30},'
        '{"id":2,"name":"Bob","age":17},'
        '{"id":3,"name":"Charlie","age":25}]. '
        "Print the result as JSON."
        # Safety note: 'pure' intent — but the framework cannot enforce this.
        # A misconfigured system_message or adversarial prompt can cause the
        # LLM to deviate and execute unintended code.
    ),
    llm_config=llm_config,
)

filter_agent = AssistantAgent(
    name="FilterAgent",
    system_message=(
        "You are a data filtering agent. "
        "When given a user list as JSON, write and execute Python code that "
        "filters to adults only (age >= 18) and returns "
        '{"adults": [...], "count": N}. '
        "Print the result as JSON."
        # No output schema enforcement; the LLM may format differently each run.
    ),
    llm_config=llm_config,
)

report_agent = AssistantAgent(
    name="ReportAgent",
    system_message=(
        "You are a reporting agent. "
        "When given adult user data as JSON, write and execute Python code that "
        'returns {"report": "Found N adults: Name1, Name2", "status": "ok"}. '
        "Print the result as JSON, then say exactly: PIPELINE_COMPLETE"
        # Termination is string-matched — brittle if LLM adds punctuation.
    ),
    llm_config=llm_config,
)

# ── Group chat wiring ─────────────────────────────────────────────────────────
# AutoGen's GroupChat manages turn order. For a sequential pipeline we use
# round_robin — but the framework does not enforce data-flow dependencies.
# If filter_agent fires before data_agent has produced output, it will attempt
# to filter an empty dataset and produce garbage.

groupchat = GroupChat(
    agents=[orchestrator, data_agent, filter_agent, report_agent],
    messages=[],
    max_round=12,
    speaker_selection_method="round_robin",
)

manager = GroupChatManager(groupchat=groupchat, llm_config=llm_config)


# ── Entrypoint ─────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    orchestrator.initiate_chat(
        manager,
        message=(
            "Run the three-step pipeline: "
            "1) DataGenerator: generate the user list. "
            "2) FilterAgent: filter to adults. "
            "3) ReportAgent: generate the summary report. "
            "Execute each step in order and pass JSON output between steps."
        ),
    )
    # NOTE: runtime token cost per execution (gpt-4o-mini, approximate):
    #   system prompts:  ~250 tokens × 3 agents  =  ~750 tokens
    #   conversation:    ~150 tokens × 6 turns    =  ~900 tokens
    #   code generation: ~100 tokens × 3 steps    =  ~300 tokens
    #   Total runtime:   ~1,950 tokens  (vs. 0 for Aether — no LLM at runtime)
